"use strict";

// !Логічні оператори
let xNumber = 100;
let yNumber = 200;

let xString = "100";
let yString = "200";

// Більше
console.log(xNumber > yNumber, "xNumber > yNumber");

// Менше
console.log(xNumber < yNumber, "xNumber < yNumber");

// Більше рівне
console.log(xNumber >= yNumber, "xNumber >= yNumber");

// Менше рівне
console.log(xNumber <= yNumber, "xNumber <= yNumber");

// Присвоєння
// console.log(xNumber = yNumber, "xNumber = yNumber");

// Дорівнює
console.log(xNumber == yNumber, "xNumber == yNumber");

// Не дорівнює
console.log(xNumber != yNumber, "xNumber != yNumber");

// Строге дорівнює
console.log(xNumber === xString, "Строге дорівнює xNumber === xString");


// Строге не дорівнює
console.log(xNumber !== xString, "Строге не дорівнює xNumber !== xString");

// Логічне ні
console.log(!(xNumber == xString));

// Логічне АБО (or)
console.log(xNumber + yNumber == 0 || xStringxNumber + yNumber >= 200);
// *true and true = true
// *true and false = true
// *false and false = false


// Логічне І (and)
console.log(xNumber + yNumber == 0 && xStringxNumber + yNumber >= 200);
// *true and true = true
// *true and false = false
// *false and false = false


